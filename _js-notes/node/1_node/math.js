const add = (a, b) => {
    return a + b;
};
const minus = (a, b) => {
    return a - b;
};
const multiply = (a, b) => {
    return a * b;
};
const divide = (a, b) => {
    return a / b;
};
const reminder = (a, b) => {
    return a % b;
};

module.exports = { add, minus, multiply, divide, reminder };